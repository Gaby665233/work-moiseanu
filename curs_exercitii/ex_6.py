# Să creăm împreună un script Python pentru Alex care înregistrează numărul total de clienți pentru ianuarie și februarie, /
# apoi să calculăm numărul mediu de clienți pe lună folosind operatorii aritmetici.

# Numărul total de clienţi în ianuarie: 1200
# Numărul total de clienţi în februarie: 950
# Acum să calculăm suma și valoarea medie. Putem folosi operatorii aritmetici pentru a rezolva această sarcină?

Numărul_ianuarie = 1200
Numărul_februarie = 950
suma_total = Numărul_ianuarie + Numărul_februarie
valoarea_med = suma_total / 2
print(f"Suma totala este: {suma_total}")
print(f"valoarea medie este: {valoarea_med}")
